function [SAMPLE_ADDR_WIDTH-1:0] preset_base;
    input [2:0] preset_id;
    begin
        case(preset_id)
        3'd1: preset_base = 10'd0;
        3'd2: preset_base = 10'd140;
        3'd3: preset_base = 10'd280;
        3'd4: preset_base = 10'd420;
        3'd5: preset_base = 10'd560;
        3'd6: preset_base = 10'd700;
        3'd7: preset_base = 10'd840;
        default: preset_base = 10'd0;
        endcase
    end
endfunction

function [2:0] sanitize_preset;
    input [2:0] preset_id;
    begin
        if(preset_id >= 3'd1 && preset_id <= 3'd7)
            sanitize_preset = preset_id;
        else
            sanitize_preset = 3'd1;
    end
endfunction

reg [3:0]  curr_state;
reg [31:0] cnt;
reg [9:0]  sample_index;
reg [SAMPLE_CYCLES_WIDTH-1:0] sample_cycles;
reg [31:0] cnt_carrier;
reg        carrier;
reg [2:0]  active_preset;

wire sample_mark;
wire sample_done;
wire start_accept;
wire [2:0] requested_preset;
wire [SAMPLE_ADDR_WIDTH-1:0] active_base;
wire [SAMPLE_ADDR_WIDTH-1:0] requested_base;

assign busy = (curr_state != S_IDLE);
assign debug_state = curr_state;
assign debug_bit_cnt = sample_index;
assign start_accept = start && !busy;
assign sample_mark = (sample_index[0] == 1'b0);
assign sample_done = (cnt >= sample_cycles - 1'b1);
assign requested_preset = sanitize_preset(preset);
assign active_base = preset_base(active_preset);
assign requested_base = preset_base(requested_preset);

always @(posedge clk or negedge rst_n) begin
    if(!rst_n) begin
        cnt_carrier <= 0;
        carrier <= 1'b0;
    end else if(curr_state == S_IDLE) begin
        cnt_carrier <= 0;
        carrier <= 1'b0;
    end else if(cnt_carrier >= CARRIER_CNT - 1) begin
        cnt_carrier <= 0;
        carrier <= 1'b0;
    end else begin
        cnt_carrier <= cnt_carrier + 1'b1;
        carrier <= (cnt_carrier < CARRIER_HIGH);
    end
end

always @(posedge clk or negedge rst_n) begin
    if(!rst_n) begin
        curr_state <= S_IDLE;
        cnt <= 0;
        sample_index <= 0;
        sample_cycles <= 0;
        active_preset <= 3'd1;
        ir_pwm <= 1'b0;
        done <= 1'b0;
    end else begin
        ir_pwm <= 1'b0;
        done <= 1'b0;

        case(curr_state)
            S_IDLE: begin
                cnt <= 0;
                sample_index <= 0;
                if(start_accept) begin
                    active_preset <= requested_preset;
                    sample_cycles <= sample_cycles_rom[requested_base];
                    curr_state <= S_PLAY;
                end
            end

            S_PLAY: begin
                if(sample_mark)
                    ir_pwm <= carrier;

                if(sample_done) begin
                    cnt <= 0;
                    if(sample_index == SAMPLE_COUNT - 1) begin
                        sample_index <= 0;
                        sample_cycles <= sample_cycles_rom[active_base];
                        curr_state <= S_IDLE;
                        done <= 1'b1;
                    end else begin
                        sample_index <= sample_index + 1'b1;
                        sample_cycles <= sample_cycles_rom[active_base + sample_index + 1'b1];
                    end
                end else begin
                    cnt <= cnt + 1'b1;
                end
            end

            default: begin
                curr_state <= S_IDLE;
                cnt <= 0;
                sample_index <= 0;
                sample_cycles <= 0;
                active_preset <= 3'd1;
            end
        endcase
    end
end

